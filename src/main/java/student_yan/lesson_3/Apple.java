package student_yan.lesson_3;

class Apple {

    private String type;
    private String colour;
    private int weight;

    public Apple(String type, String colour, int weight) {
        this.type = type;
        this.colour = colour;
        this.weight = weight;
    }

    public String getType() {
        return this.type;

    }

    public String getColour() {
        return this.colour;
    }

    public int getWeight() {
        return this.weight;

    }
}
